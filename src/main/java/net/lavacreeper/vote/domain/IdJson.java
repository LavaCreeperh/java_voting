package net.lavacreeper.vote.domain;

public class IdJson {
    Integer id;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "IdJson{" +
                "id=" + id +
                '}';
    }
}
